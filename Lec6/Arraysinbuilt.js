var a = [1,2,3,4];

console.log(a);

// push operation - > add at the last
a.push(10);
console.log(a);

//pop is an operation which is deleting the last element of an array
a.pop();

console.log(a);

a.pop();
console.log(a);


// unsihft -> it is adding the element at the begining of an array 

a.unshift(0);
console.log(a);

// shift -> removing the element from the begining of an array 

a.shift();
console.log(a);
